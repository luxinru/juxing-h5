import $ from "jquery";

export default {
  name: "AboutPage",

  data() {
    return {
      intervalId: null,
    };
  },

  mounted() {
    this.$nextTick(() => {
      this.init();

      $("#development_history_4 li").hover(function () {
        $("#development_history_4 li .circle::before").animate(
          {
            height: "118px",
          },
          "slow"
        );
      });

      var timer = 3000;
      var vv = $("#development_history_4 li");
      var aa = $(vv).length;
      var index = 0;
      var addClassOn;
      addClassOn = setInterval(function () {
        if (index >= aa) {
          index = 0;
        } else {
          $(vv).eq(index++).addClass("on").siblings().removeClass("on");
        }
      }, timer);

      $(vv).mouseenter(function () {
        clearInterval(addClassOn); //清除计时
        $(vv).removeClass("on");
        $(this).addClass("on");
      });

      $(vv).mouseleave(function () {
        clearInterval(addClassOn); //清除计时
        addClassOn = setInterval(function () {
          if (index >= aa) {
            index = 0;
          } else {
            $(vv).eq(index++).addClass("on").siblings().removeClass("on");
          }
        }, timer);
      });
    });
  },

  beforeUnmount() {
    clearInterval(this.intervalId);
  },

  methods: {
    init() {
      const $img_list = this.$refs.img_list;
      const scrollWidth = $img_list.scrollWidth;
      $img_list.scrollLeft = 0;

      this.intervalId = setInterval(() => {
        $img_list.scrollLeft += 1;
        if (scrollWidth - $img_list.scrollLeft === $img_list.offsetWidth) {
          $img_list.scrollLeft = 0;
        }
      }, 10);
    },
  },
};
</script>

